    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        // TODO add your handling code here:
        if (tableClientes.getSelectedRow() == -1) {
            JOptionPane.showMessageDialog(null, "Nenhum Registro Selecionado na Tabela");
            return;
        }
        DefaultTableModel modelo = (DefaultTableModel) tableClientes.getModel();
        int dialogButton = JOptionPane.YES_NO_OPTION;
        int dialogResult = JOptionPane.showConfirmDialog(this, "Deseja Excluir o Registro Selecionado?", "Atenção!", dialogButton);
        if (dialogResult == 0) {
            //sim                       
            String data = (String) tableClientes.getValueAt(tableClientes.getSelectedRow(), 0);
            int id = Integer.parseInt(data);
            ClienteDao dao = new ClienteDao();
            try {
                if (dao.Deletar(id)) {
                    JOptionPane.showMessageDialog(null, "Registro removido com Sucesso!", "Atenção", JOptionPane.INFORMATION_MESSAGE);
                }
            } catch (IOException ex) {
                Logger.getLogger(listaClientes.class.getName()).log(Level.SEVERE, null, ex);
            }
            modelo.removeRow(tableClientes.getSelectedRow());
        } else {

        }
    }//GEN-LAST:event_btnExcluirActionPerformed

    private void preencheTabela() throws IOException {
        DefaultTableModel modelo = (DefaultTableModel) tableClientes.getModel();
        modelo.setNumRows(0);
        ClienteDao dao = new ClienteDao();
        for (Cliente c : dao.ListarTodos()) {
            modelo.addRow(new String[]{
                "" + c.getID(),
                c.getNome(),
                c.getRg(),
                c.getCpf(),
                c.getCarteiraMotorista()
            });
        }
    }

