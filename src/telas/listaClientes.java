    private void btnAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlterarActionPerformed
        // TODO add your handling code here:
        if (tableClientes.getSelectedRow() == -1) {
            JOptionPane.showMessageDialog(null, "Nenhum Registro Selecionado na Tabela");
            return;
        }
        Cliente c = new Cliente();
        c.setID((String) tableClientes.getValueAt(tableClientes.getSelectedRow(), 0));
        c.setNome((String) tableClientes.getValueAt(tableClientes.getSelectedRow(), 1));
        c.setRg((String) tableClientes.getValueAt(tableClientes.getSelectedRow(), 2));
        c.setCpf((String) tableClientes.getValueAt(tableClientes.getSelectedRow(), 3));
        c.setCarteiraMotorista((String) tableClientes.getValueAt(tableClientes.getSelectedRow(), 4));

        cadastroClientes cad = new cadastroClientes(null, rootPaneCheckingEnabled, c);
        cad.setVisible(true);
        try {
            preencheTabela();
        } catch (IOException ex) {
            Logger.getLogger(listaClientes.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnAlterarActionPerformed

